    private void populatePastPatientDetails(){
        DefaultTableModel model = (DefaultTableModel) patientHistoryTable.getModel();
        model.setRowCount(0);
        for (PatientHistory ph: this.systemObj.getListOfPatientHistories()){
            Object[] row = new Object[10];

            row[0] = ph.getPatientID();
            row[1] = ph.getPatientName();
            row[2] = ph.getAge();
            row[3] = ph.getGender();
            row[4] = ph.getTemperature();
            row[5] = ph.getBloodPressure();
            row[6] = ph.getBloodGlucose();
            row[7] = ph.getPulse();
            row[8] = ph.getWeight();
            row[9] = ph.getDiagnosisComments();
            model.addRow(row);
        
    }}
 
    
